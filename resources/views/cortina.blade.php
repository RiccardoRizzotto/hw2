    <h1 class = "titolo2"> Sciare a Cortina quando la neve è high tech</h1>
    <img src="images\neve_tech.webp" class = "immagine">
     <p class = "commento"> La certezza? Potrai dire di esserti cimentato su percorsi solcati da campioni, che saranno teatro delle Olimpiadi Invernali di Milano-Cortina 2026. Ma questo solo se le tue capacità sciistiche sono tali da poterti far apprezzare, anzi gustare fino in fondo una sciata solcando gli oltre 1200 km di piste che puoi affrontare nel circuito Dolomiti Superski. Perché le Dolomiti hanno davvero tanto da offrire: 16 comprensori sciistici ad alta tecnologia (compreso l’innevamento artificiale per offrire sempre piste al top) con impianti moderni, sicuri e veloci riuniti in uno scenario da favola, tutelato dall’Unesco quale patrimonio dell’Umanità. Altrimenti, per non rischiare, puoi imparare da zero o migliorare la tua tecnica grazie alla vasta proposta di Scuole Sci e maestri qualificati, sulle piste di Cortina, tra le più belle nei comprensori dolomitici.</p>
  </div>


  <div id = "flex-container">
    <h1 class = "titolo2"> Ciaspolare a Cortina quando la neve è slow</h1>
    <img src="images\neve_slow.webp" class = "immagine">
     <p class = "commento"> Questa è davvero un’esperienza alla portata di tutti: una passeggiata nel bosco (in questo caso “ciaspolata” visto l’impiego delle racchette da neve) di circa un’ora e mezza che, dal parcheggio della piscina comunale di Cortina, ti porterà al Rifugio Mietres. Lungo il sentiero sarai circondato dal bosco e avrai come sfondo montagne meravigliose, come accade in ogni punto della conca ampezzana. Una volta arrivato al rifugio - ma il servizio è su prenotazione - avrai la possibilità di immergerti in una caratteristica vasca botte con acqua calda sulla terrazza baciata dal sole fino a sera o fare la sauna finlandese. Il valore aggiunto che non puoi prenotare? Vivere questa esperienza subito dopo una bella nevicata.</p>
  </div>

   
  <div id = "flex-container">
    <h1 class = "titolo2"> Snowboard a Cortina quando la neve è fantasia</h1>
    <img src="images\snowboard.webp" class = "immagine">
     <p class = "commento"> Anche gli amanti dello snowboard alla corte della regina delle Dolomiti troveranno tante possibilità di divertimento.
                            Ideali per i principianti sono sicuramente le piste di Pocol, mentre le aree delle 5 Torri e del Lagazuoi sono perfette per chi è già in grado di sbizzarrirsi con salti e discese mozzafiato.
                            Se invece - come chi scrive - preferisci lo snowboard… da guardare, non perdere la Coppa del Mondo di Snowboard in notturna sulle piste del Faloria, appuntamento ormai fisso e in calendario a Cortina dal 2015. </p>
  </div>


  <div id = "flex-container">
    <h1 class = "titolo2"> Pattinare a Cortina quando la neve è eleganza</h1>
    <img src="images\pattinare.webp" class = "immagine">
     <p class = "commento"> Stanco di sciare? Puoi cambiare gli sci con un bel paio di pattini “a lame”, e lanciarti sulla pista di pattinaggio, operativa in inverno come d’estate. Qui potrai pattinare in autonomia o imparare l’abc di questa disciplina con istruttori qualificati. Costruito nel 1956 per le Olimpiadi Invernali di Cortina, oggi lo Stadio Olimpico del Ghiaccio è una struttura coperta che offre vari servizi, compresi il punto di noleggio dell’attrezzatura, un bar, un solarium ed un baby park per i più piccoli</p>
  </div>


  <div id = "flex-container">
    <h1 class = "titolo2"> Sleddog a Cortina quando la neve è avventura</h1>
    <img src="images\sleddog.webp" class = "immagine">
     <p class = "commento"> In una meravigliosa conca naturale, in località Pocol a pochi minuti da Cortina, presso la Malga Peziè de Parù dopo una lezione teorica iniziale di guida, la preparazione del “mezzo” e aver ricevuto indicazioni sul comportamento degli animali potrai fare un’emozionante escursione sulla neve di circa 30 minuti alla guida di una slitta trainata da cani. Di fronte ai tuoi occhi si apriranno scenari altrimenti impossibili da raggiungere, in cui potrai godere della magnificenza delle vette dolomitiche da punti di vista unici.</p>
