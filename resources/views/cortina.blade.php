         <p class = "commento"> Con una storia millenaria e secoli di tradizione turistica, incastonata in uno degli scenari di montagna più suggestivi al mondo, <a href="https://www.comunecortinadampezzo.bl.it/"> Cortina</a> d’Ampezzo è la regina delle Dolomiti, il cui regno in inverno offre panorami imbiancati di rara bellezza e - soprattutto - chilometri di piste e strutture di ogni tipo per misurarsi con ogni sport invernale. Poco importa che tu sia alle prime armi o un provetto sciatore, perché qui troverai il giusto mix di discese dolci e pendii mozzafiato, piste da affrontare con le ciaspole o magari su una slitta trainata da cani… Basti pensare che lo Sci Club Cortina è uno dei più antichi d’Italia, fondato nel 1903 ed è stato insignito dal CONI della Stella d’Oro al Merito Sportivo.</p>
