        <img src="https://www.altabadia.org/media/titelbilder/corvara-by-alexmoling-3.jpg" class = "immagine">
             <p class = "commento"> Corvara, situata a 1.568 m sul livello del mare in una conca soleggiata ai piedi del monte Sassongher, può vantare una lunga tradizione turistica.

                Grazie alla sua posizione privilegiata sulla Sellaronda, il giro intorno al massiccio del Sella, in inverno con gli sci ed in estate con bici da strada o mtb, Corvara è sicuramente il luogo ideale per le tue vacanze all’insegna dello sport.
                
                Il punto di forza dell’offerta turistica di Corvara è l’elevata qualità dell’offerta alberghiera, gastronomica e dei servizi. Ristoranti di lusso, pasticcerie, vita notturna, negozi alla moda e centri noleggio offrono un servizio di prima classe.
                
                Corvara offre una vasta scelta di alloggi: dall'hotel a 5 e 4 stelle fino al confortevole albergo e agriturismo e B&B a gestione famigliare o agli appartamenti e accoglienti bar e pasticcerie.
                
                Da diversi anni Corvara è anche meta amata da un vasto turismo internazionale, senza pregiudicare la tranquillità e l’intimità del piccolo paese di montagna. </p>
