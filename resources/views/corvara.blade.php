  <div id = "flex-container">
    <h1 class = titolo3> La prima scuola di sci e il primo impianto di risalita </h1>
    <img src="https://www.altabadia.org/media/titelbilder/col-alto-vintage.jpg" class = "immagine">
     <p class = "commento"> Già nella seconda metà del XVIII secolo alcuni giovani del paese accompagnavano i primi escursionisti sulle vette delle Dolomiti. Erano principalmente studiosi di scienze naturali e geologi provenienti dalla Gran Bretagna, particolarmente interessati alla natura unica delle Dolomiti. Negli anni ’30 fu fondata a Corvara la prima scuola di sci della Val Badia e nel 1938 venne costruito il primo impianto di risalita al Col Alto, una slittovia. Alla slittovia seguì nel 1946 la seggiovia Col Alto, la prima in Italia. Con la seggiovia Col Alto, entrata definitivamente in servizio nel 1947 ha inizio l’era sciistica dell’Alta Badia. Oggi al Col Alto ci si arriva con una moderna cabinovia.</p>
  </div>


  <div id = "flex-container">
    <h1 class = titolo3> Corvara d'inverno </h1>
    <img src="https://www.altabadia.org/media/titelbilder/ski-sella-alex-moling.jpg" class = "immagine">
     <p class = "commento"> In inverno Corvara è attrezzata con una rete di moderni impianti di risalita. Le tante piste da sci rosse e blu intorno a Corvara sono la gioia degli sciatori più tranquilli e delle famiglie con bambini. Gli sciatori più esperti possono, invece, lanciarsi su piste più impegnative come la Vallon – Boè (4,5 km) e la Col Alt, e accedere direttamente a due degli itinerari sciistici più belli dell'arco alpino come il giro della Sellaronda ed il "Giro sciistico della Grande Guerra".</p>
  </div>


  <div id = "flex-container">
    <h1 class = titolo3> Corvara d'estate </h1>
    <img src="https://www.altabadia.org/media/titelbilder/lago-bo-by-daniel-taipale.jpg" class = "immagine">
     <p class = "commento"> In estate due moderni impianti di risalita, che partono dal centro di Corvara, permettono di raggiungere facilmente quote oltre i 2.000 metri, dove godere di straordinari panorami sulle Dolomiti dell'Alta Badia e fino al ghiacciaio della Marmolada e alle cime delle Alpi austriache. Gli escursionisti e gli scalatori hanno a disposizione sentieri ben segnalati a fondovalle e sull'altopiano del Col Alt – Pralongià, una vasta scelta di percorsi più lunghi nel parco naturale Puez-Odle e salite più impegnative fino alla cima del Sassongher e del Piz Boè.</p>
