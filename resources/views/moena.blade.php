    <h1 class = "titolo2"> Cosa Mangiare a Moena</h1>
    <img src="images\neve_tech.webp" class = "immagine">
     <p class = "commento"> La montagna riserva sempre un fascino particolare, e il Trentino Alto Adige è la regione giusta se pensate di rilassarvi e di provare qualche piatto tipico della tradizione. Se arrivate nella Val di Fassa e fate un salto a Moena, avrete la possibilità di scoprire i piatti tipici del territorio, sia in estate che in inverno. Subendo la vicinanza e la dominazione dell’Austria, Moena (come del resto il Trentino) ha assorbito diversi sapori. Se vi va la pasta potete assaggiare i Ciajonciè di Moena che sono dei ravioli ripieni di fichi secchi e pane nero, oppure gli Strangolapreti che sono degli gnocchi fatti con pane raffermo, uova, spinaci e grana del Trentino. Come secondo vi segnaliamo i Canederli, delle polpette fatte con uova, latte e pane raffermo, ripiene di lucaniche (salsicce) oppure di salame, speck, formaggio o erbe. Molto particolari e saporiti sono i prodotti derivanti dal latte come il “Puzzone di Moena”, un formaggio DOP tipico del Trentino Alto Adige. Per finire con un bel dolce, vi consigliamo il tradizionale Strudel di Mele oppure le Fortaie o Strauben, delle frittelle tipicamente tirolesi a forma di chiocciola fatte con farina, uova, latte, burro, zucchero e un goccio di grappa che dà un sapore aromatico.</p>
  </div>


  <div id = "flex-container">
    <h1 class = "titolo2"> Cosa fare a Moena</h1>
    <img src="https://images.placesonline.com/photos/moena_city__1601047367.jpg" class = "immagine">
     <p class = "commento"> Uno degli eventi assolutamente immancabili di Moena è la Marcialonga, la più importante competizione di sci di fondo italiana. Il tracciato attraversa la Val di Fassa e la Val di Fiemme e la gara si tiene l’ultima domenica di gennaio, con partenza dalla piana di Moena fino a Canazei, poi discesa a Molina di Fiemme e arrivo in piazza a Cavalese. Quando la neve scarseggia si organizzano delle competizioni accorciate. Ispirata alla leggendaria Vasaloppet svedese, la Marcialonga attira ogni anno migliaia di partecipanti, ed è stata anche inserita in Coppa del Mondo.</p>
  </div>

   
  <div id = "flex-container">
    <h1 class = "titolo2"> Come si vive </h1>
    <img src="https://images.placesonline.com/photos/moena_autumn__1601047367.jpg" class = "immagine">
     <p class = "commento"> Dai suoi esordi agricoli, l’economia di Moena, così come quella di tutta la Val di Fassa, è passata quasi integralmente al turismo, che oggi costituisce di gran lunga la prima fonte di redito per il paese. Non manca però chi ancora pratica l’allevamento, tanto che il Puzzone di Moena è riconosciuto come uno dei formaggio più tipici e apprezzati di tutto il Nord-Est italiano. Il turismo in Val di Fassa si può considerare nato insieme all’interesse geologico per le Dolomiti, quando gli straordinari “Monti pallidi” cominciarono ad attirare studiosi da tutta Europa; di lì a poco li seguirono gli alpinisti, che tracciarono le prime vie ferrate e resero queste montagne una meta popolare per gli amanti delle escursioni.

        Moena iniziò ad essere frequentata anche da personalità illustri, come Re Alberto I del Belgio. Il resto arrivò grazie alla costruzione dei comprensori e degli impianti sciistici, che ancora oggi sono tra i migliori del Trentino. A Moena sono anche molto attivi gli artigiani in particolare nella produzione di oggetti in legno e mobili, con decorazioni artistiche di ogni tipo, soprattutto sculture. Fra le curiosità, non si può non citare la presenza del rione cosiddetto Turchia di Moena: ha preso il suo nome, secondo la tradizione, dall’arrivo di un generale dell’Impero Ottomano sopravvissuto alla disfatta del suo esercito durante l’assedio di Vienna del 1529, soccorso dagli abitanti che poi vennero appunto chiamati “i Turchi”. La “Festa in Turchia” è un altro dei grandi eventi del comune, quando con costumi e tradizioni tipicamente orientali si ricorda questo leggendario avvenimento (più probabilmente, il nome deriva dalla presenza nell’abitato di molti torchi).</p>
